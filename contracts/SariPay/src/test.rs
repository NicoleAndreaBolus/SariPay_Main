#[test]
fn test_5_distributor_cancel_with_refund() {
    let (env, contract_id, client, merchant, distributor, token, token_admin) = setup_test();

    token_admin.mint(&merchant, &500);

    let order_id = 5u64;
    let amount = 300i128;

    client.init_order(&order_id, &merchant, &distributor, &token.address, &amount);
    client.fund_order(&order_id);

    client.cancel_order(&order_id);

    assert_eq!(token.balance(&merchant), 500);

    let key = DataKey::Order(order_id);
    
    let stored_order: SupplyOrder = env.as_contract(&contract_id, || {
        env.storage().instance().get(&key).unwrap()
    });

    assert_eq!(stored_order.status, OrderStatus::Canceled);
}